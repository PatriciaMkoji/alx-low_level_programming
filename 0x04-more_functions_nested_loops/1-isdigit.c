#include "main.h"
#include <stdio.h>
#include <ctype.h>

/**
 * _isdigit - checks if first character is digit
 * @c : charcter to be tested
 * Return: 1 is digit 0 if otherwise
 */
int _isdigit(int c)
{
	if ((c >= '0') && (c <= '9'))
	{
	return (1);
	}
	else
	{
	return (0);
	}
}



























